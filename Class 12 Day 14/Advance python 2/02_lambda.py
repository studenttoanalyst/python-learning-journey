# LAMBDA FUNCTION
"""function created by using an expression using lambda keyword"""
# syntax
"""
lambda arguments:expressions

# can be used as normal function
"""

# Example
# Find square of a number

square = lambda x:x*x
result=square(6)
print(result)

# find sum

sum = lambda a,b,c:a+b+c
sum(1,2,3)
result = sum(1,2,3)
print(result)